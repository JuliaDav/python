a = [5, 7, 5, 8, 9, 10]
print(a)
def sort(a):
    i = 0
    while i < (len(a)-1):
        if a[i] < a[i+1]:
            a[i], a[i+1] = a[i+1], a[i]
            if i >0:
                i = i-1
        elif a[i] > a[i+1]:
            i += 1
        else:
            i += 1
    return a
print(sort(a))


