# Задание-1:
# Напишите функцию, возвращающую ряд Фибоначчи с n-элемента до m-элемента.
# Первыми элементами ряда считать цифры 1 1

def fib (a,z):
    l = [1, 1]
    res = []
    i = 0
    while l[-1] < int(z):
        l.append(l[i] + l[i+1])
        i += 1
    for el in l:
        if a < int(el) < z:
            res.append(el)
    return res

print(fib(10, 1000))


